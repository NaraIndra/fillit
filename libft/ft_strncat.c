#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*ps1;

	ps1 = s1;
	i = 0;
	while (*ps1)
		ps1++;
	while (i < n && (*(ps1 + i) = *(s2 + i)))
		i++;
	*(ps1 + i) = '\0';
	return (s1);
}
