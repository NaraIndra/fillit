#include "libft.h"

void		ft_strreverse(char *str)
{
	int	i;
	int	j;
	int c;

	i = 0;
	j = ft_strlen(str) - 1;
	while (i < j)
	{
		c = str[i];
		str[i] = str[j];
		str[j] = c;
		i++;
		j--;
	}
}
